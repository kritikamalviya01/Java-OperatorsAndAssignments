public class ComparisionOperator {
    public static void main(String[] args) {
        System.out.println("value of a is " + (int)'a');
        System.out.println("value of A is " + (int)'A');

        System.out.println(10 <= 20);
        System.out.println('a' < 'A');
        System.out.println('a' < 900);
    }
}
