public class UnaryOperator {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("a = "+ a);
        // Pre Increment
        System.out.println(++a);
        System.out.println("a = "+ a);
        // Post Increment
        System.out.println(a++);
        System.out.println("a = "+ a);
        // Pre Decrement
        System.out.println(--a);
        System.out.println("a = "+ a);
        // Post Decrement
        System.out.println(a--);
        System.out.println("a = "+ a);
    }
}